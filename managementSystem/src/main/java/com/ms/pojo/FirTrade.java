package com.ms.pojo;

public class FirTrade {
	
	private int firTradeId;
	private String firTradeDesc;
	private int priority;
	public int getFirTradeId() {
		return firTradeId;
	}
	public void setFirTradeId(int firTradeId) {
		this.firTradeId = firTradeId;
	}
	public String getFirTradeDesc() {
		return firTradeDesc;
	}
	public void setFirTradeDesc(String firTradeDesc) {
		this.firTradeDesc = firTradeDesc;
	}
	public int getPriority() {
		return priority;
	}
	public void setPriority(int priority) {
		this.priority = priority;
	}
	
}
